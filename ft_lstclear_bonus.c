#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	if (!lst)
		return ;
	while (*lst)
	{
		temp = (*lst)->next;
		if (!temp)
			return ;
		del(temp->content);
		free(*lst);
		*lst = NULL;
	}
}
